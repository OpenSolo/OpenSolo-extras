#ifndef MEMORY_MAP_H_
#define MEMORY_MAP_H_

/*
Name	Start		End			Use
H		0x3D8000	0X3DBFFF	Flash Params
G		0x3DC000	0x3DFFFF	Application
F		0x3E0000	0x3E3FFF	Application
E		0x3E4000	0x3E7FFF	Application
D		0x3E8000	0x3EBFFF	Application
C		0x3EC000	0x3EFFFF	Application
B		0x3F0000	0x3F3FFF	Application
A		0x3F4000	0x3F7FFF	Bootloader
 */

#define SECTOR_H_START	0x3D8000
#define SECTOR_H_END	0X3DBFFF
#define SECTOR_G_START	0x3DC000
#define SECTOR_G_END	0x3DFFFF
#define SECTOR_F_START	0x3E0000
#define SECTOR_F_END	0x3E3FFF
#define SECTOR_E_START	0x3E4000
#define SECTOR_E_END	0x3E7FFF
#define SECTOR_D_START	0x3E8000
#define SECTOR_D_END	0x3EBFFF
#define SECTOR_C_START	0x3EC000
#define SECTOR_C_END	0x3EFFFF
#define SECTOR_B_START	0x3F0000
#define SECTOR_B_END	0x3F3FFF
#define SECTOR_A_START	0x3F4000
#define SECTOR_A_END	0x3F7FFF

#define APP_START		SECTOR_G_START
#define APP_END			SECTOR_B_END


#endif /* MEMORY_MAP_H_ */
